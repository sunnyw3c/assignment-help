                                <p class="text-gray-700 leading-relaxed">
                                    Structured, comprehensive reviews following explicit protocols (PRISMA, Cochrane) with transparent search strategies, inclusion criteria, quality assessment, and data extraction. Common in healthcare, education, and social sciences. Includes 30-100+ peer-reviewed studies with flow diagrams and summary tables.
                                </p>
                            </div>

                            <div class="bg-white rounded-lg p-5 shadow-sm">
                                <h4 class="text-xl font-bold text-blue-600 mb-3">Scoping Reviews</h4>
                                <p class="text-gray-700 leading-relaxed">
                                    Exploratory reviews mapping existing literature to identify research gaps, clarify concepts, and determine feasibility of systematic reviews. Broader in scope than systematic reviews, useful for emerging topics or interdisciplinary areas where evidence is scattered across diverse sources.
                                </p>
                            </div>

                            <div class="bg-white rounded-lg p-5 shadow-sm">
                                <h4 class="text-xl font-bold text-blue-600 mb-3">Critical Literature Reviews</h4>
                                <p class="text-gray-700 leading-relaxed">
                                    Analytical reviews that evaluate and critique theoretical frameworks, methodological approaches, and research paradigms. Go beyond summary to assess strengths, limitations, assumptions, and biases in existing research. Common in theoretical dissertations and conceptual papers.
                                </p>
                            </div>

                            <div class="bg-white rounded-lg p-5 shadow-sm">
                                <h4 class="text-xl font-bold text-blue-600 mb-3">Integrative Reviews</h4>
                                <p class="text-gray-700 leading-relaxed">
                                    Comprehensive reviews that synthesize theoretical and empirical literature to generate new frameworks, conceptual models, or research agendas. Combine diverse methodologies and perspectives to create integrated understanding. Used for theory development and generating research questions.
                                </p>
                            </div>

                            <div class="bg-white rounded-lg p-5 shadow-sm">
                                <h4 class="text-xl font-bold text-blue-600 mb-3">Meta-Analyses</h4>
                                <p class="text-gray-700 leading-relaxed">
                                    Statistical synthesis of quantitative research findings from multiple studies to calculate overall effect sizes, assess heterogeneity, and draw robust conclusions. Requires statistical expertise using specialized software (R, Comprehensive Meta-Analysis). Gold standard for evidence synthesis in many fields.
                                </p>
                            </div>

                            <div class="bg-white rounded-lg p-5 shadow-sm">
                                <h4 class="text-xl font-bold text-blue-600 mb-3">Theoretical Literature Reviews</h4>
                                <p class="text-gray-700 leading-relaxed">
                                    Reviews focused on examining theoretical frameworks, models, and conceptual developments within a field. Analyze how theories have evolved, identify theoretical gaps, and establish theoretical foundations for empirical research. Essential for dissertation theoretical frameworks.
                                </p>
                            </div>

                            <div class="bg-white rounded-lg p-5 shadow-sm">
                                <h4 class="text-xl font-bold text-blue-600 mb-3">Methodological Literature Reviews</h4>
                                <p class="text-gray-700 leading-relaxed">
                                    Reviews examining research methods, measurement instruments, analytical techniques, or design approaches used to study particular phenomena. Identify methodological strengths, limitations, innovations, and gaps. Useful for justifying methodological choices in research proposals.
                                </p>
                            </div>

                            <div class="bg-white rounded-lg p-5 shadow-sm">
                                <h4 class="text-xl font-bold text-blue-600 mb-3">Annotated Bibliographies</h4>
                                <p class="text-gray-700 leading-relaxed">
                                    Organized lists of sources with descriptive and evaluative annotations for each entry. Summarize key arguments, assess credibility, evaluate relevance, and explain how sources relate to research topic. Foundation for developing full literature reviews.
                                </p>
                            </div>

